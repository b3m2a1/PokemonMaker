function PToS(I,L){
    var s=" ";
    var t=" ";
    var cc=" ";
    var c=" ";
    var m=0;
    s=I;
    t="";
    m=L;
    for(var i=0;i<m;i+=1){
     if (i>=s.length){
      cc="";
     }else{
      c=s.substr(i*2,2);
      c=c.toUpperCase();
      if(c=="FF"){
       cc="";
       i=m+1;
      }else{
       cc=PokeToChar(c);
      }
     }
     t+=cc;
    }
    return t;
}

